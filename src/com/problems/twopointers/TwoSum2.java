package com.problems.twopointers;

//Problem - 167
public class TwoSum2 {
    public int[] twoSum(int[] numbers, int target) {
        int right = numbers.length - 1, left = 0;
        while (left < right) {
            int sum = numbers[left] + numbers[right];
            if (sum > target) {
                right--;
            } else if (sum < target) {
                left++;
            } else {
                return new int[]{left + 1, right + 1};
            }
        }
        return new int[]{0, 0};
    }
}
